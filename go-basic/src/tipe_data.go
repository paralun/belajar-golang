package main

import (
	"fmt"
	"strings"
)

func main() {
	// Number
	fmt.Println(1)
	fmt.Println(100)
	fmt.Println(10.6)

	// Boolean
	fmt.Println(true)
	fmt.Println(false)

	// String
	fmt.Println("Ini String")
	fmt.Println(len("ABC"))
	fmt.Println(strings.ToUpper("upper"))
	fmt.Println(strings.ToLower("LOWER"))
}
